// ── Stage → SpriteSet mapping ───────────────────────────

const STAGE_SPRITES: Record<PetStage, SpriteSet> = {
  egg: eggSprites,
  baby: babySprites,
  young: fullSprites,
  teen: fullSprites,
  adult: fullSprites,
}

// ── Fallback action mapping ─────────────────────────────
// Some actions don't have dedicated sprites; fall back to similar ones.

function resolveAction(action: PetAction): string {
  switch (action) {
    case 'run':    return 'walk'
    case 'talk':   return 'idle'
    case 'climb':  return 'walk'
    case 'fly':    return 'happy'
    default:       return action
  }
}

// ── Public API ──────────────────────────────────────────

export interface SpriteData {
  pixels: number[][]
  palette: Palette
  size: number
}

export function getSprite(stage: PetStage, action: PetAction, frame: number): SpriteData {
  const spriteSet = STAGE_SPRITES[stage]
  const resolved = resolveAction(action)
  const frames = spriteSet.frames[resolved] ?? spriteSet.frames['idle']
  const idx = frame % frames.length

  return {
    pixels: frames[idx],
    palette: spriteSet.palette,
    size: spriteSet.size,
  }
}

export function getFrameCount(stage: PetStage, action: PetAction): number {
  const spriteSet = STAGE_SPRITES[stage]
  const resolved = resolveAction(action)
  const frames = spriteSet.frames[resolved] ?? spriteSet.frames['idle']
  return frames.length
}
